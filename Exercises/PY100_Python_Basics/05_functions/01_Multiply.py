"""

Examine the function invocation below. Write the function multiply, such that it accepts two arguments and returns the product of its arguments. 
You may assume that the function arguments will always be numbers.

multiply(12, 4)      # 48

"""


# My response:

def multiply(num1, num2):       # Expects two arguments to be passed to the function and assigned to num1 and num2
    product = num1 * num2       # Multiplies the two arguments and assigns the value to product
    return product              # Returns the value of product to the function invokement

multiply(12, 4)      # 48      
